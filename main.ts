joystickbit.onButtonEvent(joystickbit.JoystickBitPin.P14, joystickbit.ButtonType.down, function () {
    joystickbit.Vibration_Motor(100)
    basic.showString("E")
    basic.pause(100)
    basic.clearScreen()
    radio.sendValue("Back", 1)
})
