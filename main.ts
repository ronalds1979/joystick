joystickbit.initJoystickBit()
joystickbit.Vibration_Motor(100)
radio.setGroup(7)
radio.sendNumber(7)
basic.forever(function () {
    basic.showString("" + (joystickbit.getRockerValue(joystickbit.rockerType.X)))
    basic.pause(100)
    basic.showString("" + (joystickbit.getRockerValue(joystickbit.rockerType.Y)))
    basic.pause(100)
    radio.sendValue("X", joystickbit.getRockerValue(joystickbit.rockerType.X))
    radio.sendValue("Y", joystickbit.getRockerValue(joystickbit.rockerType.Y))
})
